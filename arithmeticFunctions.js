module.exports.add = function add(value1, value2) {
    return value1 + value2;
}

module.exports.sub = function sub(value1, value2) {
    return value1 - value2;
}

module.exports.multiply = function multiply(value1, value2) {
    return value1 * value2;
}

module.exports.divide = function divide(value1, value2) {
    return value1 / value2;
}
